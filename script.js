function applyLanguage(lang) {
  const dict = translations[lang] || translations.zh;
  document.querySelectorAll("[data-i18n]").forEach((node) => {
    const key = node.getAttribute("data-i18n");
    if (dict[key]) {
      node.textContent = dict[key];
    }
  });

  const navMap = {
    home: dict.nav_home,
    company: dict.nav_company,
    business: dict.nav_business,
    investment: dict.nav_investment,
    certification: dict.nav_certification,
    roadmap: dict.nav_roadmap,
    qna: dict.nav_qna
  };

  document.querySelectorAll("[data-nav]").forEach((item) => {
    const key = item.getAttribute("data-nav");
    if (navMap[key]) {
      item.textContent = navMap[key];
    }
  });

  document.documentElement.lang = lang === "zh" ? "zh-CN" : lang;
  document.querySelectorAll(".lang-btn").forEach((btn) => {
    btn.classList.toggle("active", btn.dataset.target === lang);
  });
  localStorage.setItem("site-lang", lang);
}

function markActiveMenu() {
  const page = document.body.getAttribute("data-page");
  document.querySelectorAll(".main-nav a, .m-nav a").forEach((link) => {
    link.classList.toggle("active", link.getAttribute("data-nav") === page);
  });
}

function injectFloatingActions() {
  const file = window.location.pathname.split("/").pop() || "index.html";
  const isMobile = file.startsWith("m.");
  const map = {
    "index.html": "m.index.html",
    "company.html": "m.company.html",
    "business.html": "m.business.html",
    "investment.html": "m.investment.html",
    "certification.html": "m.certification.html",
    "roadmap.html": "m.roadmap.html",
    "qna.html": "m.qna.html",
    "m.index.html": "index.html",
    "m.company.html": "company.html",
    "m.business.html": "business.html",
    "m.investment.html": "investment.html",
    "m.certification.html": "certification.html",
    "m.roadmap.html": "roadmap.html",
    "m.qna.html": "qna.html"
  };
  const switchHref = map[file];
  if (!switchHref) return;

  const wrap = document.createElement("div");
  wrap.className = "floating-actions";

  const switchBtn = document.createElement("a");
  switchBtn.className = "floating-btn";
  switchBtn.href = switchHref;
  switchBtn.setAttribute("data-i18n", isMobile ? "desktop_view" : "mobile_view");
  wrap.appendChild(switchBtn);

  if (document.body.getAttribute("data-page") !== "qna") {
    const qnaBtn = document.createElement("a");
    qnaBtn.className = "floating-btn floating-btn-qna";
    qnaBtn.href = isMobile ? "m.qna.html" : "qna.html";
    qnaBtn.setAttribute("data-i18n", "qna_quick");
    wrap.appendChild(qnaBtn);
  }

  document.body.appendChild(wrap);
}

function initQnaContact() {
  const role = document.getElementById("qna-role");
  const go = document.getElementById("qna-go");
  const status = document.getElementById("qna-status");
  if (!role || !go) return;

  const contacts = {
    lawyer: { name: "James Ho", phone: "+6499161198", email: "james@jlaw.co.nz" },
    accountant: { name: "Lucy Xiao", phone: "+64223067329", email: "info@grca.co.nz" },
    adviser: { name: "Irene Zheng", phone: "+64276686088", email: "irene.zheng@@loanmarket.co.nz" },
    planner: { name: "Cozy Tsui", phone: "+6492651356", email: "cozy@topland.co.nz" }
  };

  function setStatus(key, tone) {
    if (!status) return;
    const lang = localStorage.getItem("site-lang") || "zh";
    const dict = translations[lang] || translations.zh;
    status.textContent = dict[key] || "";
    status.classList.remove("is-error", "is-success");
    if (tone) {
      status.classList.add(tone);
    }
  }

  function logInquiry(payload) {
    const body = JSON.stringify(payload);

    if (navigator.sendBeacon) {
      const blob = new Blob([body], { type: "application/json" });
      const sent = navigator.sendBeacon("/api/qna-contact", blob);
      return Promise.resolve(sent);
    }

    return fetch("/api/qna-contact", {
      method: "POST",
      headers: { "Content-Type": "application/json" },
      body,
      keepalive: true
    }).then((response) => response.ok);
  }

  go.addEventListener("click", async () => {
    const selectedRole = role.value;
    const methodNode = document.querySelector("input[name='qna-method']:checked");
    const method = methodNode ? methodNode.value : "phone";
    const target = contacts[selectedRole];
    if (!target) return;

    setStatus("qna_status_sending");

    const payload = {
      role: selectedRole,
      method,
      language: localStorage.getItem("site-lang") || "zh",
      page: document.body.getAttribute("data-page") || "qna"
    };

    try {
      const ok = await logInquiry(payload);
      if (ok) {
        setStatus(method === "phone" ? "qna_status_ready_phone" : "qna_status_ready_email", "is-success");
      } else {
        setStatus("qna_status_failed", "is-error");
      }
    } catch {
      setStatus("qna_status_failed", "is-error");
    }

    if (method === "phone") {
      window.location.href = `tel:${target.phone}`;
      return;
    }

    const subject = encodeURIComponent(`Inquiry to ${target.name}`);
    window.location.href = `mailto:${target.email}?subject=${subject}`;
  });
}

